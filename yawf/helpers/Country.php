/**
 * Return the name of a country in a particular language (default is "en").
 * Feel free to add country names in other languages as you need them for
 * your application.
 *
 * @author Kevin Hutchinson <kevin@guanoo.com>
 */
class Country extends YAWF
{
    /**
     * Return the name associated with a country code, in a language
     *
     * @param String $code the country code to lookup
     * @param String $language the language to use (default is "en")
     * @return String the name of the country in the language
     */
    public static function name($code, $language = 'en')
    {
        $code = self::parse_code($code);
        return array_key(self::$codes[$language], $code);
    }

    /**
     * Return an array of country names and codes in a language
     *
     * @param String $language the language to use (default is "en")
     * @return Array the array of country names and codes in the language
     */
    public static function names($language = 'en')
    {
        return array_flip(array_key(self::$codes, $language));
    }

    /**
     * Return an array of country codes and names in a language
     *
     * @param String $language the language to use (default is "en")
     * @return Array the array of country codes and names in the language
     */
    public static function codes($language = 'en')
    {
        return array_key(self::$codes, $language);
    }

    /**
     * Return the time zone associated with a country code
     *
     * @param String $code the country code to lookup
     * @return Integer the country's time zone
     */
    public static function time_zone($code, $state = NULL)
    {
        $code = self::parse_code($code);
        if ($state) $state = self::parse_code($state);
        return ($code == 'US' && is_string($state)  ?
                array_key(self::$state_tz, $state)  :
                array_key(self::$country_tz, $code));
    }

    /**
     * Parse a country code by turning 3-chars into 2-chars (in uppercase)
     *
     * @param String $code the country code to parse
     * @return String the parsed country code (2-chars in uppercase or NULL)
     */
    private static function parse_code($code)
    {
        $code = strtoupper($code);
        if (strlen($code) == 3) $code = array_key(self::$trans, $code);
        return $code;
    }

