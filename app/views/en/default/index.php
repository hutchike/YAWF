<p>
    Welcome to <acronym title="Yet Another Web Framework">YAWF</acronym>, a simple lightweight <acronym title="Model View Controller">MVC</acronym> framework written in <a href="http://www.php.net/">PHP 5</a>.
</p>

<p>
    YAWF contains only about 5k lines of code and delivers these top 10 features:
    <ol>
        <li>Standard <acronym title="Model View Controller">MVC</acronym> architecture</li>
        <li>Integrated testing (unit, integration, UI)</li>
        <li>Support for multiple page layouts and various content types</li>
        <li>Extensibility via "dependency injection" and "drop-in" class replacements</li>
        <li>Models that support "has_a", "has_many" and "belongs_to" relationships</li>
        <li>Models that support remote data conections via REST web services</li>
        <li>Various database connectors (currently supports MySQL and SQLite)</li>
        <li>Simple REST web services for models and other web services</li>
        <li>Integrated PHP shell ("<acronym title="Yet Another SHell">yash</acronym>") for scripting and testing</li>
        <li>Full detailed logging and error reporting by email</li>
    </ol>
</p>

